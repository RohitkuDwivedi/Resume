<h2 >
    1. A great resume makes you look more professional
</h2>
<p font-family: verdana;
    font-size: 30px;>
When a hiring manager sees what he considers to be a great resume, it instantly inspires positive thoughts about you as an employment candidate. Your well-structured resume gives the impression that you're an organized person. Your concise resume language gives the feeling that you're a no-nonsense individual who gets right to the point. A great resume can make a hiring manager feel like you have all of the necessary traits to be an asset to the company.
</p>
<h1 >2. A great resume is easier for the hiring manager to read.</h1>
<p>A great resume is one that has all of your important information structured in a way that's easy for the hiring manager to read. When you have a truly great resume, then that increases your chances for getting more interviews for positions that you're truly interested in. Since the resume is easier for the hiring manager to read, it inspires the hiring manager to consider your resume</p>
<h1 >3. A great resume speaks directly to each specific hiring manager.</h1>
<p font-family: verdana;
    font-size: 30px;>One of the elements that makes a resume great is a custom message. There needs to be portions of your resume that directly address a hiring manager's needs. For example, your statement of qualifications can mention the company's open position specifically and point out particular elements of your past that make you uniquely qualified for the position. When you speak directly to each hiring manager, your resumes will be much more effective.</p>
<h2  > 4. Ditch the Objective Statement </h2>
<p font-family: verdana;
    font-size: 30px;>According to Zhang , the only occasion when an objective section makes sense is when you’re making a huge career change and need to explain from the get-go why your experience doesn’t match up with the position you’re applying to. In every other case? Consider whether a summary statement would be right for you —or just nix it altogether to save space and focus on making the rest of your resume stellar.
</p>
<h2 >5. Keep it (Reverse) Chronological</h2>
<pfont-family: verdana;
    font-size: 30px;>There are lots of different ways to organize the information on your resume, but the good old reverse chronological (where your most recent experience is listed first) is still your best bet. Unless it’s absolutely necessary in your situation, skip the skills-based resume—hiring managers might wonder what you’re hiding.</p>

